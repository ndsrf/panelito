/**
 * GET /api/sessions/by-code/:code
 * Public endpoint — no auth required.
 * Returns limited session info for the guest join landing page.
 *
 * T-03-03: Returns ONLY { id, title, creator_display_name, status }.
 * Does NOT return creator_id, encrypted_api_key, ai_response_count, etc.
 * The creator's display name is fetched from auth.users via the service client.
 */
sessionsRouter.get('/by-code/:code', async (c) => {
  const { code } = c.req.param()
  const supabase = createServiceClient()

  try {
    // T-03-03: Explicitly select ONLY safe fields — never return creator_id or encrypted fields
    const { data: session, error } = await supabase
      .from('sessions')
      .select('id, title, status, creator_id')
      .eq('short_code', code)
      .single()

    if (error || !session) {
      return c.json({ error: 'not_found' }, 404)
    }

    // Fetch creator's display name from auth.users via admin API
    let creatorDisplayName: string | null = null
    try {
      const { data: adminUser } = await supabase.auth.admin.getUserById(session.creator_id)
      creatorDisplayName =
        adminUser?.user?.user_metadata?.full_name ??
        adminUser?.user?.email ??
        null
    } catch {
      // Non-fatal: display name is best-effort
    }

    // T-03-03: Return only the safe subset — do NOT include creator_id
    return c.json({
      id: session.id,
      title: session.title,
      creator_display_name: creatorDisplayName,
      status: session.status,
    }, 200)
  } catch (err) {
    return c.json({ error: toClientError(err) }, 500)
  }
})

/**
 * POST /api/sessions/by-code/:code/guests
 * Issues an anonymous Supabase token for a guest joining a session.
 * Public endpoint — this IS the authentication endpoint for guests.
 *
 * SESS-04: Guests enter via join link with display name only, no registration.
 * SESS-10: Guest token is returned for localStorage persistence.
 * D-03: Guests can join even closed/frozen sessions (workspace shows read-only mode).
 *
 * Rate limit: max 20 guest joins/minute per IP (T-03-06).
 *
 * T-03-01: Anonymous tokens have a unique `sub` claim; RLS on messages INSERT
 * requires auth.uid() = author_id, so guests cannot impersonate each other.
 */
sessionsRouter.post('/by-code/:code/guests', async (c) => {
  const { code } = c.req.param()

  // T-03-06: per-IP rate limit on guest joins
  const clientIp = c.req.header('x-forwarded-for') ?? c.req.header('x-real-ip') ?? 'unknown'
  if (!checkRateLimit(guestJoinBuckets, clientIp, MAX_GUEST_JOINS_PER_MINUTE)) {
    return c.json({ error: 'rate_limited' }, 429)
  }

  const GuestJoinSchema = z.object({
    display_name: z.string().min(1).max(40).trim(),
  })

  let body: unknown
  try {
    body = await c.req.json()
  } catch {
    return c.json({ error: 'invalid_json' }, 400)
  }

  const parsed = GuestJoinSchema.safeParse(body)
  if (!parsed.success) {
    return c.json({ error: 'validation_error', issues: parsed.error.issues }, 400)
  }

  const supabase = createServiceClient()

  try {
    // Look up session by code (D-03: guests can join closed/frozen sessions)
    const { data: session, error: sessionError } = await supabase
      .from('sessions')
      .select('id, title, status')
      .eq('short_code', code)
      .single()

    if (sessionError || !session) {
      return c.json({ error: 'not_found' }, 404)
    }

    // Mint a fresh anonymous Supabase user for the guest (SESS-04)
    // signInAnonymously() creates a new user in auth.users with aud: 'authenticated'
    const { data: anonData, error: anonError } = await supabase.auth.signInAnonymously()

    if (anonError || !anonData.user || !anonData.session) {
      console.error('[sessions] signInAnonymously error:', anonError?.message)
      return c.json({ error: 'internal' }, 500)
    }

    return c.json({
      session_id: session.id,
      guest_user_id: anonData.user.id,
      access_token: anonData.session.access_token,
      refresh_token: anonData.session.refresh_token,
      display_name: parsed.data.display_name,
    }, 201)
  } catch (err) {
    return c.json({ error: toClientError(err) }, 500)
  }
})

export default sessionsRouter
