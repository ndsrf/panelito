import { Hono } from 'hono'
import { z } from 'zod'
import { SessionCreateInputSchema } from '@panelito/types'
import { createServiceClient } from '../lib/supabase'
import { requireAuth, type AuthVariables } from '../middleware/auth'

// -----------------------------------------------------------------------
// Rate limiting (T-03-05, T-03-06)
// In-memory token buckets keyed by user ID or IP.
// Phase 1 uses in-memory; Redis-backed is post-MVP.
// -----------------------------------------------------------------------

interface TokenBucket {
  tokens: number
  lastRefill: number
}

const sessionCreateBuckets = new Map<string, TokenBucket>()
const guestJoinBuckets = new Map<string, TokenBucket>()

const MAX_SESSION_CREATES_PER_MINUTE = 10
const MAX_GUEST_JOINS_PER_MINUTE = 20
const MINUTE_MS = 60_000

function checkRateLimit(
  buckets: Map<string, TokenBucket>,
  key: string,
  maxPerMinute: number
): boolean {
  const now = Date.now()
  let bucket = buckets.get(key)

  if (!bucket) {
    bucket = { tokens: maxPerMinute - 1, lastRefill: now }
    buckets.set(key, bucket)
    return true
  }

  // Refill tokens if a minute has passed
  const elapsed = now - bucket.lastRefill
  if (elapsed >= MINUTE_MS) {
    bucket.tokens = maxPerMinute
    bucket.lastRefill = now
  }

  if (bucket.tokens <= 0) {
    return false // Rate limited
  }

  bucket.tokens--
  return true
}

// -----------------------------------------------------------------------
// Error helper (T-03-07)
// Wraps unexpected Supabase errors — no raw error messages leak to client.
// -----------------------------------------------------------------------

function toClientError(err: unknown): string {
  if (err instanceof Error) {
    // Log for server-side debugging only
    console.error('[sessions] Internal error:', err.message)
  }
  return 'internal'
}

// -----------------------------------------------------------------------
// Sessions router
// -----------------------------------------------------------------------

export const sessionsRouter = new Hono<{ Variables: AuthVariables }>()

/**
 * POST /api/sessions
 * Creates a new session for the authenticated creator.
 *
 * SESS-02: Creator creates session with title and mode.
 * The short_code is generated by the Postgres generate_short_code() function.
 *
 * Rate limit: max 10 creates/minute per user (T-03-05).
 */
sessionsRouter.post('/', requireAuth, async (c) => {
  const user = c.get('user')

  // T-03-05: per-user rate limit on session creation
  if (!checkRateLimit(sessionCreateBuckets, user.id, MAX_SESSION_CREATES_PER_MINUTE)) {
    return c.json({ error: 'rate_limited' }, 429)
  }

  let body: unknown
  try {
    body = await c.req.json()
  } catch {
    return c.json({ error: 'invalid_json' }, 400)
  }

  const parsed = SessionCreateInputSchema.safeParse(body)
  if (!parsed.success) {
    return c.json({ error: 'validation_error', issues: parsed.error.issues }, 400)
  }

  const supabase = createServiceClient()

  try {
    // short_code is generated by the DB trigger (generate_short_code() function).
    // We must call select('*') after insert to get the generated value.
    const { data, error } = await supabase
      .from('sessions')
      .insert({
        creator_id: user.id,
        title: parsed.data.title ?? null,
        mode: parsed.data.mode ?? null,
        short_code: await generateShortCodeViaRpc(supabase),
      })
      .select()
      .single()

    if (error) {
      console.error('[sessions] INSERT error:', error.message)
      return c.json({ error: toClientError(error) }, 500)
    }

    return c.json(data, 201)
  } catch (err) {
    return c.json({ error: toClientError(err) }, 500)
  }
})

/**
 * Generate a short code using the Postgres RPC function.
 * Falls back to an in-process generator if RPC fails.
 */
async function generateShortCodeViaRpc(
  supabase: ReturnType<typeof createServiceClient>
): Promise<string> {
  const { data, error } = await supabase.rpc('generate_short_code')
  if (error || !data) {
    // Fallback: generate in-process
    const alphabet = 'ABCDEFGHJKLMNPQRSTUVWXYZ23456789'
    return Array.from(
      { length: 6 },
      () => alphabet[Math.floor(Math.random() * alphabet.length)]!
    ).join('')
  }
  return data as string
}

/**
 * GET /api/sessions/:id
 * Returns the session for the authenticated user (creator or guest with anon token).
 *
 * Supabase anonymous tokens have aud: 'authenticated' so requireAuth
 * accepts both creator JWTs and guest anonymous JWTs.
 */
sessionsRouter.get('/:id', requireAuth, async (c) => {
  const { id } = c.req.param()
  const supabase = createServiceClient()

  try {
    const { data, error } = await supabase
      .from('sessions')
      .select('*')
      .eq('id', id)
      .single()

    if (error || !data) {
      return c.json({ error: 'not_found' }, 404)
    }

    return c.json(data, 200)
  } catch (err) {
    return c.json({ error: toClientError(err) }, 500)
  }
})

/**
 * POST /api/sessions/:id/freeze
 * Freezes the session. Only the creator can freeze.
 *
 * SESS-05: Creator can manually freeze session.
 * T-03-02: Non-creator gets 403; creator_id check is in addition to RLS.
 *
 * SESS-08: When session is frozen, guests cannot send messages
 * (RLS requires status = 'active' on messages INSERT).
 */
sessionsRouter.post('/:id/freeze', requireAuth, async (c) => {
  const { id } = c.req.param()
  const user = c.get('user')
  const supabase = createServiceClient()

  try {
    // Fetch the session first to verify creator_id (T-03-02)
    const { data: session, error: fetchError } = await supabase
      .from('sessions')
      .select('id, creator_id, status')
      .eq('id', id)
      .single()

    if (fetchError || !session) {
      return c.json({ error: 'not_found' }, 404)
    }

    // T-03-02: Elevation of Privilege protection
    if (session.creator_id !== user.id) {
      return c.json({ error: 'forbidden' }, 403)
    }

    const { data: updated, error: updateError } = await supabase
      .from('sessions')
      .update({ status: 'frozen' })
      .eq('id', id)
      .select()
      .single()

    if (updateError || !updated) {
      return c.json({ error: toClientError(updateError) }, 500)
    }

    return c.json(updated, 200)
  } catch (err) {
    return c.json({ error: toClientError(err) }, 500)
  }
})

